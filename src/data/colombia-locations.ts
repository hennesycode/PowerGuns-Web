export function getCities(departmentName: string): string[] {
  const dept = colombiaDepartments.find(
    (d) => d.name.toLowerCase() === departmentName.toLowerCase(),
  );
  return dept?.cities ?? [];
}

export function searchDepartments(query: string): Department[] {
  if (!query) return colombiaDepartments;
  const q = query.toLowerCase();
  return colombiaDepartments.filter((d) => d.name.toLowerCase().includes(q));
}

export function searchCities(departmentName: string, query: string): string[] {
  const cities = getCities(departmentName);
  if (!query) return cities;
  const q = query.toLowerCase();
  return cities.filter((c) => c.toLowerCase().includes(q));
}
